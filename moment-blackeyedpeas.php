	        <p class="histoire">A chaque fois que j’entends cette chanson, je me revois en novembre 2009.<br><br>
J’étais en deuxième année de régendat en sciences humaines et en novembre, on devait faire un stage de deux semaines dans une école.
Quelques jours avant le début du stage, je n’avais toujours pas trouvé d’école, ce qui signifiait que j’allais rater mon année. Je ne pouvais pas me le permettre.
Après avoir téléphoné à plusieurs écoles de la région, j’ai enfin eu mon stage.
L’étape la plus dure n’était pas de trouver un stage, loin de là! Il fallait préparer ses leçons et ça, moralement, c’était très dur. Les profs de la haute école nous démontaient à chaque rencontre, on devait détruire notre travail pour le reconstruire différemment. C’était l’horreur. Bref, cette épreuve passée, il était temps de commencer à donner cours.<br><br>
L’école était à environ 10 minutes de chez moi. Tous les jours au matin, il fallait que j’écoute I Gotta Feeling pendant tout le trajet. Je l’écoutais en boucle. Si je ne l’entendais pas, je me disais que je raterais ma journée, que je raterais mon stage. Je changeais un peu les paroles, j’avais le sentiment que ce serait un bon jour. <br><br>Et ça l’était à chaque fois.<br><br> Sur le chemin du retour, je l’écoutais au minimum une fois, pour en quelque sorte «remercier» la chanson de la bonne journée que j’avais passé. <br><br>
En deux semaines, j’ai du écouter au minimum trente fois la chanson et je ne m’en lassais pas, c'était devenu ma chanson porte bonheur.</p>
	        
	        
