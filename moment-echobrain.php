	        <p class="histoire">Pour situer le contexte, Echobrain c’est un groupe de pop-rock dirigé par Jason Newsted, ancien bassiste de Metallica. En tant que grande fan de Metallica, et je l’avoue un peu beaucoup amoureuse de leur bassiste à l’époque (bon ok, encore un peu maintenant), j’ai suivi l’artiste dans ses nouvelles compositions lorsqu’il a quitté le groupe. C’était en 2001 (l’album d’Echobrain quant à lui doit dater de 2002).<br><br>
Comme tout fan qui se respecte, j’ai écouté ce nouveau groupe en boucle dès que j’ai eu l’album. Je l’ai surtout beaucoup fait tourner lors d’un voyage scolaire en 5ème secondaire. On est allé à Prague en car avec l’école. Le voyage était long, il fallait bien s’occuper. A l’époque, c’est d’un « discman » que j’étais équipée, un SONY, ah qu’il était beau ! Evidemment, on ne savait pas avoir autant de musique avec soi qu’aujourd’hui, j’avais donc préparé une dizaine, tout au plus, de mes meilleurs cd du moment. Il devait y avoir l’album Smash d’Offspring, S&M de Metallica (forcément, je partais nulle part sans une dose de Metallica), deux-trois cd gravés par mon cousin avec Garbage, The Cranberries, Oasis … (toute une époque !) et mon dernier trésor : cet album d’Echobrain. Au final, c’est celui-là qui a tourné en boucle dans le car tant à l’aller qu’au retour.<br><br>
</p>

