Aujourd’hui, quand un peu par hasard, je retombe sur des morceaux d’Echobrain, je me revois directement dans ce car, à l’avant sous ma couverture (ça caillait à l’avant, l’air conditionné allait à fond) à côté de Marianne et devant Adrien et Jérémy qui nous enviaient la place. Ils ont d’ailleurs fait un échange forcé pour le retour … ils ont eu de l’espace mais les pauvres, ils étaient frigorifiés. Bah tant pis, on leur avait dit ! <br><br>

Ce sont donc plein plein plein de souvenirs et de paysages qui me reviennent en tête lorsque je réécoute le groupe aujourd’hui.</p>

