	        <p class="histoire">Ma grand-mère paternelle adorait Pierre Rapsat, elle l’écoutait souvent dans sa cuisine. Elle disait tout le temps « à mon enterrement, je voudrais que vous passiez cette chanson ».<br><br>
Mon grand-père, lui, a dépéri très lentement. Tellement que pendant bien deux ans mon père me disait : « aller, viens avec nous chez grand-papa et grand-maman parce que vu l’état de grand-papa ça sera peut-être la dernière fois ». Il se trouve que ça n’était jamais la dernière fois et que c’était pire à chaque fois qu’on le voyait, si bien qu’on y croyait que c’était la dernière … Bref, lorsque c’est arrivé, c’était presque un soulagement. J’ai d’ailleurs vu le visage de mon père beaucoup plus détendu du jour au lendemain. A la cérémonie, ma grand-mère a voulu que l’on passe la chanson de Pierre Rapsat. Logique.<br><br>
Ma grand-mère, elle, s’est occupée toute sa vie de son mari, ses enfants et ses petits-enfants et lorsque mon grand-père est parti c’est comme si elle n’avait plus de raison de vivre. 15 jours après le décès de grand-papa, grand-maman a été admise à l’hôpital, elle perdait la tête. C’était à noël. Elle a passé les fêtes de fin d’année à la clinique. Finalement, après deux semaines elle est allée en maison de revalidation. Là, elle faisait de la kiné, etc. Elle se reprenait en mains. Lorsque je lui ai rendu visite, j’ai eu l’impression de la voir 10 ans auparavant. C’était génial ! Et puis du jour au lendemain, ça n’a plus été. Les infirmiers l’ont trouvée par terre, ils n’ont jamais su ce qui était arrivé. <br><br>
Retour à l’hôpital. Là, elle était en plein délire, attachée au lit sinon elle en tombait. Elle était couverte de bleus. Quand j’ai été la voir, elle disait dans une sorte de demi sommeil « tu m’as laissée, tu m’avais dit qu’on irait ensemble ». J’ai compris qu’elle parlait de mon grand-père. Elle est décédée fin janvier, mon grand-père début décembre. Je ne sais pas ce qu’elle a fait ou pas fait, mais je sais qu’inconsciemment elle ne voulait pas être sans lui et que son corps a suivi sa tête… <br><br>
Au funérarium je me suis écroulée. Quand je l’ai vue dans le cercueil, j’ai complètement craqué. J’ai fait l’ascenseur émotionnel en quelques semaines. Je l’avais vue tellement bien, tellement en forme quelques semaines avant que je ne pouvais pas admettre qu’elle soit décédée. C’était trop dur. J’ai mis beaucoup de temps à l’accepter. 
A l’enterrement, comme elle le désirait, on a repassé cette chanson de Pierre Rapsat. <br><br>
Aujourd’hui je ne peux que l’associer au décès de mes grands-parents dans ce contexte si brusque (en tout cas pour ma grand-mère). Même lors du mariage d’une amie, lorsque la chanson a été diffusée, je n’étais pas bien, je n’ai pas su m’empêcher de pleurer de tristesse alors que le contexte était hyper joyeux. <br><br>
Aujourd’hui ça reste une épreuve de l’entendre.</p>
	        
