	        <p class="histoire">2008 a été l’année des « jubilés » dans ma famille. Je venais d’avoir 20 ans, mon papa allait en avoir 50 quelques jours plus tard et, cela faisait 25 ans que mes parents étaient ensembles…. et toujours pas mariés, pour mon plus grand regret.<br><br>
Ayant connu un divorce chacun de leur côté, ils ne voulaient pas se remarier, ils n’en voyaient pas l’utilité. Personnellement, je rêvais qu’ils se marient.<br><br>
J’y ai cru lorsqu’au Noël précédent, mon papa a offert une magnifique bague à ma maman. Je me souviens lui avoir dit que généralement, avec une bague pareille, une demande en mariage devait suivre. Ce ne fut pas le cas… en tout cas, c’est ce que j’ai cru jusqu’au 10 mai!<br><br>
Mes parents voulaient organiser une grande fête pour les 50 ans de mon papa. Ils souhaitaient le fêter avec toute la famille et les amis, dans un endroit magnifique. Le mot d’ordre était « soirée décontractée ». <br><br>
Première surprise de la soirée, je vois arriver un prêtre que l’on connait depuis des années. J’étais étonnée qu’il soit là vu qu’il n’était jamais invité lors de fêtes de ce genre. Il m’a juste dit qu’il était là en tant qu’ami de la famille. Je l’ai cru.<br><br>
Une fois tous les invités arrivés, mes parents ont fait un discours.<br><br>
Maman a parlé un peu. Elle était fort émue, cela s’entendait dans sa voix. Je me disais que c’était bête d’être émue juste pour un anniversaire.
Ce fut ensuite à mon papa de faire son discours. Il remerciait tout le monde d’être venu. C’était également un discours rempli d’émotion et d’amour. La conclusion de son speech fut « le DJ va vous résumer tout ça en chanson ». Et c’est à ce moment que l’on a entendu les paroles « c’est aujourd’hui le jour de mon mariage ». Un de mes frères était à côté de moi. On n’en croyait pas nos oreilles! Enfin, après 25 ans, ils se mariaient! Tout le monde était bouche-bée, personne ne s’attendait à ça! Il n’y avait que le DJ, le prêtre et le bourgmestre (qui était également un ami de la famille), qui étaient au courant. J’ai appris plus tard qu’en effet, papa avait demandé maman en mariage le jour de Noël.<br><br>
Aujourd’hui, j’habite dans le centre-ville et tous les jours, j’entends retentir cette chanson au carillon d’une église. Ca reste un magnifique souvenir.</p>
	        
	        
